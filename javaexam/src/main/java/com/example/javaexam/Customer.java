package com.example.javaexam;

public class Customer {

	String id;
	String pass;
	String name;
	
	
	public void setId(String id) {
		this.id=id;
	}
	public void setPass(String pass) {
		this.pass=pass;
	}
	public void setName(String name) {
		this.name=name;
	}
	
	
	public String getId() {
		return id;
	}
	public String getPass() {
		return pass;
	}
	public String getName() {
		return name;
	}
	
	
}
